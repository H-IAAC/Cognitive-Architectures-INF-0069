    @Override
    public void initPanel(String[] param) {
        environment = (Environment) agent.getSubmodule(ModuleName.Environment);
        if (environment != null) {
            refresh();
        } else {
            logger.log(Level.WARNING,
                    "Unable to parse module {1} Panel not initialized.",
                    new Object[]{0L, param[0]});
        }
    }
    
    @Override
    public void refresh() {
        String currentAction = environment.getCurrentAction();
        exploreText.setBackground(UNSELECTED_COLOR);
        foodText.setBackground(UNSELECTED_COLOR);
        jewelText.setBackground(UNSELECTED_COLOR);
        thingText.setBackground(UNSELECTED_COLOR);
        rotateText.setBackground(UNSELECTED_COLOR);
        switch(currentAction){
            case "explore":
                exploreText.setBackground(SELECTED_COLOR);
                break;
            case "gotoFood":
                foodText.setBackground(SELECTED_COLOR);
                break;
            case "gotoJewel":
                jewelText.setBackground(SELECTED_COLOR);
                break;                    
            case "get":
                thingText.setBackground(SELECTED_COLOR);
                break;                    
            case "rotate":
                rotateText.setBackground(SELECTED_COLOR);
                break;
            default:
        }
    }
    

